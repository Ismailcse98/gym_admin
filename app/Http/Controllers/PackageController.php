     */
    public function store(Request $r)
    {
        $r->validate([
            'title'=>'required',
            'price'=>'required',
            'd_1'=>'required',
            'd_2'=>'required',
            'd_3'=>'required',
            'd_4'=>'required',
        ]);

        $result = Package::create($r->all());
        if ($result) {
            return redirect()->route('list_package');
        }
    }

